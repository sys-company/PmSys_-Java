    private void buscaGerente(String emailUser) {

        List<Map<String, Object>> login = jdbcTemplate.queryForList("SELECT * from tblConta where login = ?", emailUser);

        if (!login.isEmpty()) {
            for (Map row : login) {
                senha = (row.get("senha").toString());

                GerarLog.escreverLog("Senha Encontrada", "A",0);

            }
        } else {
            JOptionPane.showMessageDialog(null, "Usu치rio  inv치lido!", "Erro", JOptionPane.ERROR_MESSAGE);
            GerarLog.escreverLog("Usu치rio inv치lido!", "A",0);
        }

        Email email = new Email();
        email.sendEmail(emailUser, senha);

        JOptionPane.showMessageDialog(null, "Sua senha foi enviada via Email");
        TelaLogin tl = new TelaLogin();
        tl.setVisible(true);
        dispose();

    }

